public class DeclareVariable {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean c = false;
        char k = 'c';
        String h = "Ha Noi";
        System.out.println("i= " + i);
        System.out.println("f= " + f);
        System.out.println("d= " + d);
        System.out.println("c= " + c);
        System.out.println("k= " + k);
        System.out.println("h= " + h);
    }
}
